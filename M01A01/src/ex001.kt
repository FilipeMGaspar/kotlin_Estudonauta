fun main(args: Array<String>) {
    println()
    print ("Olá, \ttudo bem!")
    println("\n")
    print ("Olá, \rtudo bem!")
    println("\n")
    print ("Olá,\btudo bem!")
    println("\n")
    print ("Olá, \\tudo bem!")
    println("\n")
    print ("Olá, \"tudo\" bem!")
    println("\n")
    print ("Olá,\ntudo\nbem!")

    println("\n")
    println("Linha 1")
    println("Linha 2")
    println()
    /*
         \n     nova linha
         \t     tabulação
         \b     backspace
         \r     retorno
         \\     bara
         \"     aspas
    *
    * */
}