import kotlin.math.*

fun main(args: Array<String>) {
    /*
    *   .sqrt()         Raiz quadrada
    *   .pow()          Exponenciação
    *   .ceill()        Adondamento por excesso
    *   .roudToInt()    Arredondamento inteiro
    *   .floor()        Adondamento por defeito
    *   .max()          Máximo
    *   .min()          Minimo
    * */

    println()
    println("Raiz quadrada: ${sqrt(25f)}")
    println("Potência: ${7f.pow(2)}")
    println("Arredonda acima: ${ceil(2.123f)}")
    println("Arredonda: ${9.3.roundToInt()}")
    println("Arredonda abaixo: ${floor(8.8f)}")
    println("Maior: ")
    println("Menor: ")
    println()
}