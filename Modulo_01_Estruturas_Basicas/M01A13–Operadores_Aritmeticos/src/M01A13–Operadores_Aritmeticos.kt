fun main(args: Array<String>) {
    /*
        operadores aritméticos, em Kotlyn antes da compilação são convertidos em métodos
                                            Métodos

        +   Soma                            .plus()
        -   Subtração                       .minus()
        *   Multiplicação                   .times()
        /   Divisão                         .div()
        %   Resto da divisão inteira        .mod()

    * */

    val n1:Int = 5
    val n2:Int = 2

    println("$n1 + $n2 0 ${n1 + n2} ")
}